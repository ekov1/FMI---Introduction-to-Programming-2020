// Да се състави програма, която приема като входни данни цели 4-байтови числа,
// елементи на монотонно растяща редица, всяко от които в интервала [0;100]. 
// Програмата приключва, при първата въведена стойност,
// непринадлежаща на редицата. Отпечатайте броя на числата от редицата.

#include <iostream>

int main()
{
	int last, current;

	std::cin >> last;

	while (true)
	{
		std::cin >> current;

		if (current < last)
		{
			break;
		}
		last = current;
	}
}

