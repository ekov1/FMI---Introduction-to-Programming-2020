//Прочетете две цели числа от стандартния вход и разменете техните стойностти.
//
//а) чрез използването на трета променлива.
//
//б) само чрез операциите събиране и изваждане.
//
//в) само чрерз умножение и деление

#include <iostream>

using namespace std;

int main()
{
	int a, b;
	cin >> a >> b;
	// a)
	int temp;
	temp = a;
	a = b;
	b = temp;

	// b)
	a = a + b;
	b = a - b;
	a = a - b;

	// в)
	a = a * b;
	b = a / b;
	a = a / b;

}