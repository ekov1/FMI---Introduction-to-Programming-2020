// Реализирайте програма, която прочита цяло положително число unsigned 
// int А и отпечатва стойноста на числото А’, получено при обръщане на първите 16 бита на А.
// Вход:  19006 // 0b01001010'00111110
// Изход: 46529 // 0b10110101'11000001

#include <iostream>

int main()
{
    unsigned A;
    std::cin >> A;
    unsigned mask = 65535; // 11111111 11111111
    unsigned A_prim = A ^ mask;

    std::cout << "A': " << A_prim << std::endl;
}
